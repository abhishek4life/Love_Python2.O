n=5
#Change In Rows
#inc number
p=1
for i in range(n):
    for j in range(i+1):
        print(p,end='')
    p+=1
    print()

print()
#change in Column
#Increasing Triangle
n=5
for i in range(n):
    p=5
    for j in range(i+1):
        print(p,end='')
        p-=1
    print()

